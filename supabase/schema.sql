-- Run this in your Supabase SQL Editor to create the necessary tables

CREATE TABLE IF NOT EXISTS clients (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  industry TEXT,
  ad_account_id TEXT,
  landing_page_url TEXT,
  business_type TEXT DEFAULT 'ecommerce',
  primary_conversion_event TEXT DEFAULT 'conversions',
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Safely add columns to existing clients table if they don't exist
DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='clients' AND column_name='landing_page_url') THEN
        ALTER TABLE clients ADD COLUMN landing_page_url TEXT;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='clients' AND column_name='business_type') THEN
        ALTER TABLE clients ADD COLUMN business_type TEXT DEFAULT 'ecommerce';
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='clients' AND column_name='primary_conversion_event') THEN
        ALTER TABLE clients ADD COLUMN primary_conversion_event TEXT DEFAULT 'conversions';
    END IF;
END $$;

