CREATE TABLE IF NOT EXISTS meta_settings (
  id BIGINT PRIMARY KEY,
  ad_account_id TEXT,
  access_token TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Safely add columns to existing meta_settings table if they don't exist
DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='meta_settings' AND column_name='access_token') THEN
        ALTER TABLE meta_settings ADD COLUMN access_token TEXT;
    END IF;
    
    -- Make ad_account_id nullable if it isn't already
    ALTER TABLE meta_settings ALTER COLUMN ad_account_id DROP NOT NULL;
END $$;

